package com.demo.bean;

public class Album {
	private String a_id ;
	private String a_name;
	private String a_singer;
	private String a_img;

	
	public String getA_id() {
		return a_id;
	}
	public void setA_id(String a_id) {
		this.a_id = a_id;
	}
	public String getA_name() {
		return a_name;
	}
	public void setA_name(String a_name) {
		this.a_name = a_name;
	}
	public String getA_singer() {
		return a_singer;
	}
	public void setA_singer(String a_singer) {
		this.a_singer = a_singer;
	}
	public String getA_img() {
		return a_img;
	}
	public void setA_img(String a_img) {
		this.a_img = a_img;
	}
	
}
